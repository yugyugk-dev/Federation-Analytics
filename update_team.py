def similarity(a, b):
    return SequenceMatcher(None, a.lower(), b.lower()).ratio()

def normalize_name(name):
    return ' '.join(name.strip().split())

def find_match(conn, target_name):
    """Find best matching athlete in DB by name."""
    cursor = conn.cursor()
    cursor.execute("SELECT id, full_name FROM athletes")
    all_rows = cursor.fetchall()

    target_norm = normalize_name(target_name)

    # 1. Exact match
    for row in all_rows:
        if normalize_name(row[1]) == target_norm:
            return row[0], row[1], 1.0

    # 2. Prefix match (surname + first name, ignore patronymic)
    target_parts = target_norm.split()
    if len(target_parts) >= 2:
        prefix = ' '.join(target_parts[:2])
        for row in all_rows:
            row_parts = normalize_name(row[1]).split()
            if len(row_parts) >= 2:
                row_prefix = ' '.join(row_parts[:2])
                if row_prefix == prefix:
                    return row[0], row[1], 0.95

    # 3. Fuzzy match
    best_score = 0
    best_row = None
    for row in all_rows:
        score = similarity(target_norm, normalize_name(row[1]))
        if score > best_score:
            best_score = score
            best_row = row

    if best_score >= 0.87 and best_row:
        return best_row[0], best_row[1], best_score

    return None, None, 0.0

